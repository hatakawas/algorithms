package com.example.algorithms.leetcode;

/**
 * Definition for singly-linked list.
 *
 * Created on Nov 08, 2021.
 *
 * @author qinchuan13
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
